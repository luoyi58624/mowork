<template>
	<view>
		<Header ref="headerRef" @getData="searchData" />
		<Content :options="options" ref="contentRef" />
	</view>
</template>

<script lang="ts">
import Header from './Header.vue'
import Content from './Content.vue'
import { mapActions } from 'pinia'
import { useCompanyStore } from '@/store/company'

export default {
	name: 'list',
	components: {
		Header,
		Content
	},
	data() {
		return {
			options: {
				isSelected: false // 开启选择模式
			}
		}
	},
	methods: {
		...mapActions(useCompanyStore, ['getProjectManagers']),
		// 执行过滤条件搜索数据，每次搜索都将重置页数
		searchData(filterOption) {
			this.$refs.contentRef.getData(filterOption, true)
		},
		// 获取数据
		getData() {
			this.$refs.contentRef.getData(this.$refs.headerRef.getFilterOption())
		}
	},
	onLoad(options) {
		this.options = options
		if (this.options.isSelected) {
			this.$nextTick(() => {
				uni.setNavigationBarTitle({
					title: '选择项目'
				})
			})
		} else {
			this.$nextTick(() => {
				uni.setNavigationBarTitle({
					title: '项目管理'
				})
			})
		}
	},
	// 初始化数据和加载所有项目经理
	mounted() {
		this.getData()
		this.getProjectManagers()
	},
	// 触底加载更多
	onReachBottom() {
		this.$refs.contentRef.loadMoreData(this.$refs.headerRef.getFilterOption())
	},
	// 下拉刷新
	async onPullDownRefresh() {
		await this.$refs.contentRef.getData(this.$refs.headerRef.getFilterOption(), true)
		await this.getProjectManagers(true)
		uni.stopPullDownRefresh()
	}
}
</script>

